package com.example.demo.user;

// Enum to represent the status of a user.
public enum Status {
    // User is active.
    ACTIVE,

    // User is inactive.
    INACTIVE,

    // User account is deleted.
    DELETED,

    // User is blocked.
    BLOCKED,

    // User account is pending approval.
    PENDING,

    // User is suspended.
    SUSPENDED,

    // User is banned.
    BANNED
}
